package com.example.j22;

public class Polymorphism {

	public int add(int a,int b)
	{
		return a+b;
	}
	
	
	public int add(int a,int b,int c)
	{
		return a+b+c;	
		}
	public float add(float a,int b)
	{
	return a+b;	
	}
	
	public float add(int a,float b)
	{
		return a+b;
	}
}
